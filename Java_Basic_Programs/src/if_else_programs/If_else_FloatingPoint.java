package if_else_programs;

public class If_else_FloatingPoint {

	public static void main(String[] args) 
    {
        float fl = 5.3f;
        if (fl == 5.3)
            System.out.println("Both are equal");
        else
            System.out.println("Both are not equal");
    }
}

/*
Output-
Both are not equal
*/